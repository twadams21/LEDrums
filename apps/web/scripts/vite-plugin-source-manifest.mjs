/* Vite plugin: `virtual:source-manifest` — a build-time map from component key to
   repo-relative source path, generated by walking the real filesystem (never
   hand-maintained; a renamed/deleted component drops out of the map on the next
   build, so stale pointers surface as visible ⚠ misses in the styleguide).

   Keys are src-relative paths without extension (`lib/ui/Splitter`,
   `lib/app/views/NodeCard`, `styles/tokens`); values are repo-relative paths
   (`apps/web/src/lib/ui/Splitter.svelte`). Tests/fixtures are excluded. */
import { readdirSync } from 'node:fs';
import { join, relative, sep } from 'node:path';
import { fileURLToPath } from 'node:url';

const SRC_DIR = fileURLToPath(new URL('../src', import.meta.url));
const REPO_PREFIX = 'apps/web/src';
const VIRTUAL_ID = 'virtual:source-manifest';
const RESOLVED_ID = '\0' + VIRTUAL_ID;

const INCLUDE = /\.(svelte|ts|css)$/;
const EXCLUDE = /(\.test\.ts|\.fixture\.svelte|\.d\.ts)$/i;

function walk(dir, out) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === 'node_modules' || entry.name.startsWith('.')) continue;
    const abs = join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(abs, out);
    } else if (INCLUDE.test(entry.name) && !EXCLUDE.test(entry.name)) {
      const rel = relative(SRC_DIR, abs).split(sep).join('/');
      const key = rel.replace(/\.(svelte|ts|css)$/, '');
      out.set(key, `${REPO_PREFIX}/${rel}`);
    }
  }
}

export function buildSourceManifest() {
  const out = new Map();
  walk(SRC_DIR, out);
  // deterministic output: sorted keys
  return Object.fromEntries([...out.entries()].sort(([a], [b]) => a.localeCompare(b)));
}

export function sourceManifestPlugin() {
  return {
    name: 'ledrums:source-manifest',
    resolveId(id) {
      if (id === VIRTUAL_ID) return RESOLVED_ID;
    },
    load(id) {
      if (id !== RESOLVED_ID) return;
      const manifest = buildSourceManifest();
      return `export const manifest = ${JSON.stringify(manifest, null, 2)};\n`;
    },
  };
}
